import 'dart:io';

/// Handles the `dart run sdui add-widget <WidgetName>` command.
///
/// Scaffolds a custom SDUI widget inside the **caller's** Flutter project by
/// generating two files per widget plus a shared barrel:
///
/// ```
/// lib/
/// └── sdui_widgets/
///     ├── sdui_custom_widgets.dart         ← barrel: exports + registerCustomWidgets()
///     └── <snake_name>/
///         ├── <snake_name>_encoder.dart    ← DSL node  (pure Dart, no Flutter)
///         └── <snake_name>_builder.dart    ← Runtime builder (Flutter, JSON → Widget)
/// ```
///
/// ### Usage
/// ```bash
/// dart run sdui add-widget RatingStars
/// ```
class AddWidgetCommand {
  final String widgetName;

  const AddWidgetCommand({required this.widgetName});

  // ── Entry point ─────────────────────────────────────────────────────────────

  void run() {
    // 1. Validate
    if (widgetName.isEmpty) {
      _err('Widget name cannot be empty.');
      exit(1);
    }
    if (!_isValidDartClassName(widgetName)) {
      _err(
        '"$widgetName" is not a valid Dart class name.\n'
        '     Use PascalCase, e.g. "RatingStars" or "ProfileCard".',
      );
      exit(1);
    }

    final snakeName = _toSnakeCase(widgetName);
    _log('🛠   Scaffolding custom widget "$widgetName" ...\n');

    // 2. Create the widget sub-directory
    final widgetDir = Directory(
      '${Directory.current.path}/lib/sdui_widgets/$snakeName',
    );
    widgetDir.createSync(recursive: true);

    // 3. Write encoder (DSL — pure Dart)
    _writeFile(
      file: File('${widgetDir.path}/${snakeName}_encoder.dart'),
      content: _encoderTemplate(snakeName),
    );

    // 4. Write builder (runtime — Flutter)
    _writeFile(
      file: File('${widgetDir.path}/${snakeName}_builder.dart'),
      content: _builderTemplate(snakeName),
    );

    // 5. Create or update barrel
    _updateBarrel(snakeName);

    // 6. Success banner
    _printSuccess(snakeName);
  }

  // ── File writers ─────────────────────────────────────────────────────────────

  void _writeFile({required File file, required String content}) {
    if (!file.existsSync()) {
      file.writeAsStringSync(content);
      _log('   ✓  Created ${file.path}');
    } else {
      _log('   ⚠  Skipped (already exists): ${file.path}');
    }
  }

  // ── Template: DSL encoder ────────────────────────────────────────────────────

  String _encoderTemplate(String snakeName) =>
      '''
// AUTO-GENERATED by: dart run sdui add-widget $widgetName
// Edit freely — this file is yours.
//
// IMPORTANT: Keep this file Flutter-free (no `package:flutter` imports).
// The CLI runs it in a plain Dart VM to serialize your widget to JSON.

import 'package:sdui_library/sdui_dsl.dart';

/// DSL encoder for [$widgetName].
///
/// Use this in your DSL definition files to compose [$widgetName] nodes
/// and export them to JSON via the SDUI CLI.
///
/// ### Example
/// ```dart
/// import 'package:sdui_library/sdui_dsl.dart';
/// import 'sdui_widgets/$snakeName/${snakeName}_encoder.dart';
///
/// final _ = $widgetName(
///   key: Key('my_${snakeName}'),
///   label: 'Hello!',
/// );
/// ```
///
/// Then export:
/// ```bash
/// dart run sdui export <file_name> my_${snakeName}
/// ```
class $widgetName extends SduiNode {
  // TODO: Replace with your real widget properties.
  final String label;

  $widgetName({
    Key? key,
    required this.label,
  }) : super(key: key);

  @override
  Map<String, dynamic> toJson() => {
        'widget': '$widgetName',
        'properties': {
          'label': label, // TODO: Add more properties here.
        },
      };
}
''';

  // ── Template: Runtime builder ────────────────────────────────────────────────

  String _builderTemplate(String snakeName) =>
      '''
// AUTO-GENERATED by: dart run sdui add-widget $widgetName
// Edit freely — this file is yours.
//
// This file is the runtime renderer for [$widgetName].
// It reads the JSON produced by the DSL encoder and returns a Flutter widget.

import 'package:flutter/material.dart';

/// Builds a [$widgetName] Flutter widget from an SDUI JSON definition.
///
/// ### Expected JSON shape
/// ```json
/// {
///   "widget": "$widgetName",
///   "properties": {
///     "label": "Hello!"
///   }
/// }
/// ```
class ${widgetName}Builder {
  ${widgetName}Builder._();

  static Widget fromJson(Map<String, dynamic> json) {
    final props = (json['properties'] as Map<String, dynamic>?) ?? {};
    final label = (props['label'] as String?) ?? '';

    // TODO: Replace with your real [$widgetName] implementation.
    if (label.isEmpty) {
      return Text(
        '[$widgetName] — no content provided',
        style: const TextStyle(
          color: Colors.grey,
          fontStyle: FontStyle.italic,
        ),
      );
    }

    return Text(label);
  }
}
''';

  // ── Barrel file: create or update ───────────────────────────────────────────

  void _updateBarrel(String snakeName) {
    final barrelFile = File(
      '${Directory.current.path}/lib/sdui_widgets/sdui_custom_widgets.dart',
    );

    if (!barrelFile.existsSync()) {
      barrelFile.writeAsStringSync(_barrelTemplate(snakeName));
      _log('   ✓  Created ${barrelFile.path}');
    } else {
      _appendToBarrel(barrelFile, snakeName);
      _log('   ✓  Updated ${barrelFile.path}');
    }
  }

  String _barrelTemplate(String snakeName) =>
      '''
// AUTO-GENERATED by sdui. You can freely add your own exports below.
// Do NOT remove the `// sdui:end` comment — the CLI uses it to append entries.

import 'package:sdui_library/sdui_library.dart';

// ── Encoders (DSL — pure Dart) ────────────────────────────────────────────────
export '$snakeName/${snakeName}_encoder.dart';

// ── Builders (runtime — Flutter) ──────────────────────────────────────────────
import '$snakeName/${snakeName}_builder.dart';

/// Registers all custom SDUI widgets with [SduiParser].
///
/// Call this **once** at app startup, before any JSON is parsed:
///
/// ```dart
/// import 'package:your_app/sdui_widgets/sdui_custom_widgets.dart';
///
/// void main() {
///   registerCustomWidgets();
///   runApp(MyApp());
/// }
/// ```
void registerCustomWidgets() {
  SduiParser.registerWidget('$widgetName', ${widgetName}Builder.fromJson);
  // sdui:end — do not remove this line
}
''';

  /// Appends encoder export, builder import, and registry call for [snakeName]
  /// into an existing barrel file, using sentinel comments as insertion anchors.
  void _appendToBarrel(File barrelFile, String snakeName) {
    var content = barrelFile.readAsStringSync();

    // ── 1. Encoder export ────────────────────────────────────────────────────
    final encoderExport = "export '$snakeName/${snakeName}_encoder.dart';";
    if (!content.contains(encoderExport)) {
      // Insert after the last _encoder.dart export line found in the file.
      final lastEncoderIdx = content.lastIndexOf("_encoder.dart';");
      if (lastEncoderIdx != -1) {
        final lineEnd = content.indexOf('\n', lastEncoderIdx);
        final insertAt = lineEnd != -1 ? lineEnd + 1 : content.length;
        content =
            '${content.substring(0, insertAt)}$encoderExport\n${content.substring(insertAt)}';
      }
    }

    // ── 2. Builder import ────────────────────────────────────────────────────
    final builderImport = "import '$snakeName/${snakeName}_builder.dart';";
    if (!content.contains(builderImport)) {
      // Insert after the last _builder.dart import line found in the file.
      final lastBuilderIdx = content.lastIndexOf("_builder.dart';");
      if (lastBuilderIdx != -1) {
        final lineEnd = content.indexOf('\n', lastBuilderIdx);
        final insertAt = lineEnd != -1 ? lineEnd + 1 : content.length;
        content =
            '${content.substring(0, insertAt)}$builderImport\n${content.substring(insertAt)}';
      }
    }

    // ── 3. registerWidget call ───────────────────────────────────────────────
    final registerCall =
        "  SduiParser.registerWidget('$widgetName', ${widgetName}Builder.fromJson);";
    if (!content.contains(registerCall)) {
      const sentinel = '  // sdui:end — do not remove this line';
      if (content.contains(sentinel)) {
        content = content.replaceFirst(sentinel, '$registerCall\n$sentinel');
      }
    }

    barrelFile.writeAsStringSync(content);
  }

  // ── Utilities ────────────────────────────────────────────────────────────────

  /// Converts PascalCase/camelCase to snake_case.
  ///
  /// Examples:
  ///   `RatingStars`  → `rating_stars`
  ///   `ProfileCard`  → `profile_card`
  ///   `MyWidget`     → `my_widget`
  String _toSnakeCase(String name) => name
      .replaceAllMapped(
        RegExp(r'(?<=[a-z0-9])([A-Z])'),
        (m) => '_${m.group(1)}',
      )
      .toLowerCase();

  /// Returns `true` if [name] is a valid Dart class identifier (PascalCase).
  bool _isValidDartClassName(String name) =>
      RegExp(r'^[A-Z][A-Za-z0-9_]*$').hasMatch(name);

  // ── Output ───────────────────────────────────────────────────────────────────

  void _printSuccess(String snakeName) {
    stdout.writeln('''
╔══════════════════════════════════════════════════════╗
║  ✓  Widget scaffolded successfully!                  ║
╚══════════════════════════════════════════════════════╝
  Widget   : $widgetName
  Location : lib/sdui_widgets/$snakeName/

  Generated files:
    lib/sdui_widgets/$snakeName/${snakeName}_encoder.dart
    lib/sdui_widgets/$snakeName/${snakeName}_builder.dart
    lib/sdui_widgets/sdui_custom_widgets.dart

  Next steps:

  1. Edit the encoder — add your real widget properties:
       lib/sdui_widgets/$snakeName/${snakeName}_encoder.dart

  2. Edit the builder — implement the Flutter widget:
       lib/sdui_widgets/$snakeName/${snakeName}_builder.dart

  3. Register at app startup (add this once to main.dart):
       import \'sdui_widgets/sdui_custom_widgets.dart\';
       void main() {
         registerCustomWidgets();
         runApp(MyApp());
       }

  4. Use in a DSL file and export to JSON:
       import \'package:sdui_library/sdui_dsl.dart\';
       import \'sdui_widgets/$snakeName/${snakeName}_encoder.dart\';
       final _ = $widgetName(key: Key(\'my_key\'), label: \'Hello!\');

       dart run sdui export <file_name> my_key
''');
  }

  void _log(String msg) => stdout.writeln('  $msg');
  void _err(String msg) => stderr.writeln('\n  ✗  $msg\n');
}
